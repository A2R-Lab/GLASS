_OUTPUT_HEADER = """// Auto-generated by GLASS bench/autotune.py — do NOT hand-edit.
//
// Per-host tuning overrides. Consumed by src/nvidia/tuning_table.cuh via the
// GLASS_TUNING_TABLE_LOCAL mechanism. The shipped in-tree tuning table carries
// the per-API primary templates + heuristics + curated specializations; this
// file just appends measured specializations for THIS host. Add no namespace
// scopes — the consuming `tuning_table.cuh` includes this inside
// `namespace _glass_tuning { ... }`.
//
// Regenerate with:
//   python3 bench/autotune.py --sm AUTO
// Consume in your build with:
//   nvcc ... -DGLASS_TUNING_TABLE_LOCAL='"@INCLUDE_PATH@"' ...
"""


def emit_overrides(out_path: pathlib.Path,
                   sms: int,
                   results_by_api: dict,
                   margin: float,
                   include_path: str,
                   hostname: str):
    """results_by_api: dict[api] -> list of (shape_tuple, simt_us, cublasdx_us).
    All values can be None for the timing pair if measurement failed."""
    header = _OUTPUT_HEADER.replace("@INCLUDE_PATH@", include_path).rstrip()
    lines = [header, ""]
    lines.append(f"// Host: {hostname}    SM: sm_{sms // 10} (sms={sms})")
    lines.append(f"// Generated at {time.strftime('%Y-%m-%d %H:%M:%S %Z')}")
    lines.append(f"// Tie margin: ±{margin*100:.0f}% (ties default to SIMT)")
    lines.append("")

    total = 0
    for api in API_CONFIGS:
        results = results_by_api.get(api)
        if not results:
            continue
        lines.append(f"// ─── {api} ─" + ("─" * (72 - len(api))))
        cfg = API_CONFIGS[api]
        # Sub-microsecond measurements are unreliable on small kernels (timer
        # granularity, kernel launch failures, shared-mem overflows that
        # bail before the kernel body runs all produce 0.000us-ish values).
        # Below this floor we can't trust either leg, so we conservatively
        # default to SIMT.
        MIN_TRUSTWORTHY_US = 0.05
        for shape, simt, cdx in results:
            spec = cfg["spec_fmt"](*shape, sms)
            if simt is None or cdx is None:
                verdict = "false"
                note = "measurement failed → SIMT default"
            elif simt < MIN_TRUSTWORTHY_US or cdx < MIN_TRUSTWORTHY_US:
                verdict = "false"
                note = (f"measurement below noise floor "
                        f"(simt={simt:.3f}us, cublasdx={cdx:.3f}us; "
                        f"< {MIN_TRUSTWORTHY_US:.2f}us) → SIMT default")
            else:
                ratio = cdx / simt
                if _cublasdx_wins(simt, cdx, margin):
                    verdict = "true"
                    note = (f"cublasdx wins ({cdx:.3f}us vs simt {simt:.3f}us, "
                            f"{(1-ratio)*100:.1f}%)")
                elif ratio > (1.0 + margin):
                    verdict = "false"
                    note = (f"simt wins ({simt:.3f}us vs cublasdx {cdx:.3f}us, "
                            f"{(ratio-1)*100:.1f}%)")
                else:
                    verdict = "false"
                    note = f"tie within ±{margin*100:.0f}% → SIMT default"
            lines.append(f"template <> constexpr bool {spec}() {{ return {verdict}; }} // {note}")
            total += 1
        lines.append("")

    out_path.parent.mkdir(parents=True, exist_ok=True)
    out_path.write_text("\n".join(lines))
    print(f"\nWrote {out_path} ({total} specializations across "
          f"{sum(1 for v in results_by_api.values() if v)} APIs).")


def emit_results_md(md_path: pathlib.Path,
                    sms: int,
                    results_by_api: dict,
                    margin: float,
                    hostname: str):
    lines = [
        f"# autotune results — {hostname}, sm_{sms // 10}",
        "",
        f"_Generated by `bench/autotune.py` at "
        f"{time.strftime('%Y-%m-%d %H:%M:%S %Z')}._",
        "",
        f"Tie tolerance: ±{margin*100:.0f}% (ties default to SIMT).",
        "",
    ]
    for api in API_CONFIGS:
        results = results_by_api.get(api)
        if not results:
            continue
        cfg = API_CONFIGS[api]
        keys = cfg["shape_keys"]
        lines.append(f"## {api}")
        lines.append("")
        header = "| " + " | ".join(keys) + " | SIMT (us) | cuBLASDx (us) | Winner | Speedup |"
        sep = "|" + "|".join(["---"] * (len(keys) + 4)) + "|"
        lines.extend([header, sep])
        # See emit_overrides_file: sub-noise-floor measurements are
        # untrustworthy and we conservatively default to SIMT.
        MIN_TRUSTWORTHY_US = 0.05
        for shape, simt, cdx in results:
            shape_cells = " | ".join(str(s) for s in shape)
            if simt is None or cdx is None:
                lines.append(f"| {shape_cells} | — | — | SIMT (compile failed) | — |")
                continue
            if simt < MIN_TRUSTWORTHY_US or cdx < MIN_TRUSTWORTHY_US:
                lines.append(f"| {shape_cells} | {simt:.3f} | {cdx:.3f} | "
                             f"SIMT (sub-noise) | — |")
                continue
            if _cublasdx_wins(simt, cdx, margin):
                lines.append(f"| {shape_cells} | {simt:.3f} | {cdx:.3f} | "
                             f"**cuBLASDx** | {simt/cdx:.2f}× |")
            elif simt < cdx * (1.0 - margin):
                lines.append(f"| {shape_cells} | {simt:.3f} | {cdx:.3f} | "
                             f"**SIMT** | {cdx/simt:.2f}× |")
            else:
                lines.append(f"| {shape_cells} | {simt:.3f} | {cdx:.3f} | "
                             f"tie (→ SIMT) | — |")
        lines.append("")
    md_path.parent.mkdir(parents=True, exist_ok=True)
    md_path.write_text("\n".join(lines))
    print(f"Wrote {md_path}")


