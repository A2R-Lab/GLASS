#!/usr/bin/env python3
"""GLASS autotuner — measure SIMT vs cuBLASDx on this machine for each of the
five auto-dispatch primaries in `_glass_tuning` and emit a per-host overrides
file consumed via `GLASS_TUNING_TABLE_LOCAL`.

Usage:
    python3 bench/autotune.py [--sm AUTO|<sm>]
                              [--apis gemm,gemv,gemv_strided,gemm_strided,gemm_batched_1d]
                              [--iters 10000]
                              [--out PATH]
                              [--margin 0.05]
                              [--dry-run]

Defaults:
    --sm AUTO        — detect via nvidia-smi
    --apis           — all five round-2 primaries
    --iters 10000    — per-kernel timing iterations
    --margin 0.05    — within ±5% counts as a tie; ties default to SIMT
    --out PATH       — bench/tuning/<hostname>.cuh by default. The shipped
                       src/nvidia/tuning_table.cuh is NEVER overwritten by
                       this script — it carries the per-API primary templates
                       and curated specializations and must stay stable.

Consume the per-host output via the round-2 override mechanism:
    nvcc ... -DGLASS_TUNING_TABLE_LOCAL='"bench/tuning/<hostname>.cuh"' ...

Requires MATHDX_ROOT set (cuBLASDx headers needed for the cuBLASDx leg).
"""

import argparse
