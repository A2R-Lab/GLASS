# ───── gemm_batched_1d ──────────────────────────────────────────────────────
# Compares the 1D SIMT batched (glass::nvidia::gemm_batched_1d) against the
# 2D cuBLASDx batched (glass::nvidia::gemm_batched). The launches differ
# (1D = TC*BATCH threads; 2D = dim3(TC, BATCH)). The autotune still picks the
# faster wall-time per op — that's what cublasdx_wins_batched<> answers.
_GEMM_BATCHED_MICROBENCH = _BENCH_PREAMBLE + textwrap.dedent("""

    static const int M = {M}, N = {N}, K = {K};
    static const int BATCH = {BATCH};

    namespace glass {{ namespace nvidia {{
        DEFINE_NVIDIA_GEMM_BLOCKDIM(M, N, K, TC)
        DEFINE_NVIDIA_GEMM_BATCHED_BLOCKDIM(M, N, K, BATCH, TC)
    }}}}

    __global__ void k_simt(float** As, float** Bs, float** Cs, volatile float* sink, int iters) {{
        for (int rep = 0; rep < iters; rep++) {{
            glass::nvidia::gemm_batched_1d<float, M, N, K, BATCH, TC>(
                1.f, As, Bs, 0.f, Cs);
            __syncthreads();
            if (threadIdx.x == 0) sink[rep & 0xFF] = Cs[0][0];
            __syncthreads();
        }}
    }}

    __global__ void k_cublasdx(float** As, float** Bs, float** Cs,
                                volatile float* sink, int iters) {{
        extern __shared__ __align__(16) char smem[];
        for (int rep = 0; rep < iters; rep++) {{
            glass::nvidia::gemm_batched<float, M, N, K, BATCH, TC>(
                1.f, As, Bs, 0.f, Cs, smem);
            __syncthreads();
            if (threadIdx.x == 0 && threadIdx.y == 0) sink[rep & 0xFF] = Cs[0][0];
            __syncthreads();
        }}
    }}

    int main(int argc, char** argv) {{
        int iters = (argc > 1) ? atoi(argv[1]) : 5000;
        float *dA, *dB, *dC, *dSink;
        // SIMT batched_1d expects contiguous A/B/C arrays of BATCH*(MxN/NxK/MxK).
        cudaMalloc(&dA, BATCH * M * N * sizeof(float));
        cudaMalloc(&dB, BATCH * N * K * sizeof(float));
        cudaMalloc(&dC, BATCH * M * K * sizeof(float));
        cudaMalloc(&dSink, 256 * sizeof(float));

        // cuBLASDx gemm_batched takes ptr-arrays; build them by indexing into
        // the same contiguous buffers above.
        float **dAs, **dBs, **dCs;
        cudaMalloc(&dAs, BATCH * sizeof(float*));
        cudaMalloc(&dBs, BATCH * sizeof(float*));
        cudaMalloc(&dCs, BATCH * sizeof(float*));
        float *hAs[BATCH], *hBs[BATCH], *hCs[BATCH];
        for (int b = 0; b < BATCH; b++) {{
            hAs[b] = dA + b * M * N;
            hBs[b] = dB + b * N * K;
            hCs[b] = dC + b * M * K;
        }}
        cudaMemcpy(dAs, hAs, BATCH * sizeof(float*), cudaMemcpyHostToDevice);
        cudaMemcpy(dBs, hBs, BATCH * sizeof(float*), cudaMemcpyHostToDevice);
        cudaMemcpy(dCs, hCs, BATCH * sizeof(float*), cudaMemcpyHostToDevice);

        struct timespec t0, t1;
        constexpr size_t smem =
            glass::nvidia::gemm_batched_scratch_bytes<float, M, N, K, BATCH, TC>();

        // SIMT batched_1d launches 1D with TC*BATCH threads, ptr-array args.
        k_simt<<<1, TC * BATCH>>>(dAs, dBs, dCs, dSink, 100); cudaDeviceSynchronize();
        // cuBLASDx batched launches 2D dim3(TC, BATCH).
        k_cublasdx<<<1, dim3(TC, BATCH), smem>>>(dAs, dBs, dCs, dSink, 100);
        cudaDeviceSynchronize();

        clock_gettime(CLOCK_MONOTONIC, &t0);
        k_simt<<<1, TC * BATCH>>>(dAs, dBs, dCs, dSink, iters);
        cudaDeviceSynchronize();
        clock_gettime(CLOCK_MONOTONIC, &t1);
        printf("simt %.6f\\n", elapsed_us(t0, t1) / iters);

        clock_gettime(CLOCK_MONOTONIC, &t0);
        k_cublasdx<<<1, dim3(TC, BATCH), smem>>>(dAs, dBs, dCs, dSink, iters);
        cudaDeviceSynchronize();
        clock_gettime(CLOCK_MONOTONIC, &t1);
        printf("cublasdx %.6f\\n", elapsed_us(t0, t1) / iters);

        cudaFree(dA); cudaFree(dB); cudaFree(dC); cudaFree(dSink);
        cudaFree(dAs); cudaFree(dBs); cudaFree(dCs);
        return 0;
    }}
""")


