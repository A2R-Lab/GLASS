def emit_defaults_table(sweep_path, out_path, sms):
    """Parse a bench_mega_sweep run (mega_sweep_*.txt) and emit a per-host override
    header for glass-defaults.cuh (the warp/block/nvidia ladder). Uses the NPROB=8192
    throughput regime for f32 + f64."""
    import re
    text = pathlib.Path(sweep_path).read_text()
    bemap = {"BLOCK": "block", "WARP": "warp", "NVIDIA": "nvidia"}
    line_re = re.compile(r"^(dot|gemv|gemm|chol|trsv|posv)\s+N=(\d+)\b.*->\s+(BLOCK|WARP|NVIDIA)\b")
    hdr_re = re.compile(r"NPROB=(\d+).*dtype=(f32|f64)")
    winners = {}          # (dtype, op) -> {N: backend}
    dtype, nprob = None, None
    for line in text.splitlines():
        if line.startswith("####"):
            m = hdr_re.search(line)
            if m:
                nprob, dtype = int(m.group(1)), m.group(2)
            continue
        if nprob != 8192:
            continue
        lm = line_re.match(line.strip())
        if lm:
            op, N, be = lm.group(1), int(lm.group(2)), bemap[lm.group(3)]
            winners.setdefault((dtype, op), {})[N] = be
    if not winners:
        sys.exit(f"No NPROB=8192 verdicts parsed from {sweep_path}")

    def emit_op(op, dtype):
        picks = winners.get((dtype, op))
        if not picks:
            return None
        ns = sorted(picks)
        runs = []                          # (hi_N, backend) collapsed threshold ranges
        for N in ns:
            be = picks[N]
            if runs and runs[-1][1] == be:
                runs[-1] = (N, be)
            else:
                runs.append((N, be))
        conds = " ".join(f"if (N <= {hi}u) return backend::{be};" for hi, be in runs[:-1])
        return f"{conds} return backend::{runs[-1][1]};".strip()

    # NOTE: included by glass-defaults.cuh INSIDE namespace glass::defaults (via the
    # GLASS_DEFAULTS_TABLE_LOCAL hook), so this fragment must NOT re-open the namespace.
    lines = [
        f"// Generated by bench/autotune.py --emit-defaults from {pathlib.Path(sweep_path).name}",
        f"// (sm {sms}, NPROB=8192 throughput regime). Do not edit by hand.",
        "// Included by glass-defaults.cuh inside namespace glass::defaults — no namespace wrapper.",
        "#define GLASS_DEFAULTS_HAVE_LOCAL",
        "constexpr backend local_ideal(op o, uint32_t N, bool f64, uint32_t /*sm*/) {",
    ]
    for dtype, guard in (("f32", "if (!f64) {"), ("f64", "if (f64) {")):
        lines.append(f"    {guard}")
        for op in ("dot", "gemv", "gemm", "chol", "trsv", "posv"):
            body = emit_op(op, dtype)
            if body:
                lines.append(f"        if (o == op::{op}) {{ {body} }}")
        lines.append("    }")
    lines += ["    return backend::block;", "}", ""]
    out = pathlib.Path(out_path)
    out.write_text("\n".join(lines))
    print(f"==> wrote {out}  ({len(winners)} (dtype,op) groups)")
    print(f"    use via: -DGLASS_DEFAULTS_TABLE_LOCAL='\"{out}\"'")


def main():
    p = argparse.ArgumentParser(
        description=__doc__,
        formatter_class=argparse.RawDescriptionHelpFormatter,
    )
    p.add_argument("--sm", default="AUTO",
                   help="SM number (e.g. 860, 1200) or AUTO (detect via nvidia-smi)")
    p.add_argument("--apis", default=",".join(API_CONFIGS.keys()),
                   help=f"Comma-separated API list. Default: all five. "
                        f"Choices: {', '.join(API_CONFIGS.keys())}")
    p.add_argument("--shapes", default=None,
                   help="Override default shape grid. Format: semicolon-separated "
                        "tuples, where the arity matches each API's parameters. "
                        "Same grid applied to every API named in --apis — to "
                        "give per-API grids, run autotune separately per API.")
    p.add_argument("--iters", type=int, default=10000,
                   help="Per-kernel timed iterations (default 10000). Bump for "
                        "noisy machines or for shapes hitting the tie tolerance.")
    p.add_argument("--out", default=None,
                   help="Output path. Default: bench/tuning/<hostname>.cuh. "
                        "Use --in-tree to write to the shipped src/nvidia/tuning_table.cuh "
                        "(only do this for upstream PRs).")
    p.add_argument("--in-tree", action="store_true",
                   help="(Advanced) Write specializations to "
                        "src/nvidia/tuning_table.cuh, preserving its round-2 primary "
                        "templates and structure. For upstream-PR contributors. The "
                        "default per-host output is the right choice for almost everyone.")
    p.add_argument("--margin", type=float, default=0.05,
                   help="Tie tolerance: |1 - cublasdx/simt| <= margin counts as a "
                        "tie (default 0.05)")
    p.add_argument("--dry-run", action="store_true",
                   help="Measure but don't write any files")
    p.add_argument("--build-only", action="store_true",
                   help="Compile every microbench into the build cache and exit "
                        "(no timing, no table written). Pre-warms the cache so the "
                        "actual sweep on a quiet GPU is execute-only. Safe to run "
                        "while the GPU is busy — compilation is CPU-bound.")
    p.add_argument("--build-dir", default=None,
                   help="Persistent build cache dir (default "
                        "bench/.tune_cache/sm<sms>). Binaries are hash-keyed on the "
                        "source + library digest + SM and reused across runs.")
    p.add_argument("--emit-defaults", metavar="SWEEP_TXT", default=None,
                   help="Parse a bench_mega_sweep run (mega_sweep_*.txt) and emit a per-host "
                        "glass-defaults.cuh override header (warp/block/nvidia ladder), then exit.")
    args = p.parse_args()

    sms = detect_sm() if args.sm == "AUTO" else int(args.sm)
    hostname = platform.node().replace(" ", "_")

    if args.emit_defaults:
        out = args.out or str(TUNING_DIR / f"{hostname}_defaults.cuh")
        TUNING_DIR.mkdir(parents=True, exist_ok=True)
        emit_defaults_table(args.emit_defaults, out, sms)
        return

    requested_apis = [a.strip() for a in args.apis.split(",") if a.strip()]
    unknown = [a for a in requested_apis if a not in API_CONFIGS]
    if unknown:
        sys.exit(f"Unknown API(s): {unknown}. Choices: {list(API_CONFIGS.keys())}")

    mathdx_root_env = os.environ.get("MATHDX_ROOT")
    if not mathdx_root_env:
        sys.exit("ERROR: MATHDX_ROOT not set — autotune needs cuBLASDx headers.")
    mathdx_root = pathlib.Path(mathdx_root_env)

    build_dir = (pathlib.Path(args.build_dir) if args.build_dir
                 else BENCH_DIR / ".tune_cache" / f"sm{sms}")
    build_dir.mkdir(parents=True, exist_ok=True)

    def shapes_for(api):
        if args.shapes:
            try:
                return parse_shapes_for_api(api, args.shapes)
            except ValueError as e:
                # --shapes arity mismatches this API (common with multiple APIs
                # in --apis); skip rather than abort the whole run.
                print(f"  [skip {api}] {e}", file=sys.stderr)
                return None
        return list(API_CONFIGS[api]["default_shapes"])

    # ── build-only: compile every microbench into the cache, no timing ──
    if args.build_only:
        print(f"Prebuild: sm_{sms // 10}   cache={build_dir}")
        print(f"APIs: {', '.join(requested_apis)}   MATHDX_ROOT={mathdx_root}\n")
        tally = {"cached": 0, "built": 0, "fail": 0, "cached-fail": 0}
        for api in requested_apis:
            shapes = shapes_for(api)
            if shapes is None:
                continue
            print(f"── {api} ({len(shapes)} shapes) " + "─" * max(0, 56 - len(api)))
            for shape in shapes:
                _, status = build_shape(api, shape, sms, mathdx_root, build_dir)
                tally[status] = tally.get(status, 0) + 1
                print(f"  {API_CONFIGS[api]['label_fmt'](shape):24} {status}")
        print(f"\nPrebuilt: {tally['built']} new, {tally['cached']} already cached, "
              f"{tally['fail'] + tally['cached-fail']} cuBLASDx-rejected (SIMT default).")
        print(f"Cache ready at {build_dir} — the timed sweep is now execute-only.")
        return

    if args.in_tree:
        out = (GLASS_DIR / "src" / "nvidia" / "tuning_table.cuh").resolve()
    elif args.out is not None:
        out = pathlib.Path(args.out).resolve()
    else:
        out = (TUNING_DIR / f"{hostname}.cuh").resolve()
    md = out.parent / (out.stem + "_results.md")

    print(f"Autotune target: sm_{sms // 10} (sms={sms})  host={hostname}")
    print(f"APIs:            {', '.join(requested_apis)}")
    print(f"Iterations:      {args.iters}")
    print(f"Tie margin:      ±{args.margin*100:.0f}%")
    print(f"MATHDX_ROOT:     {mathdx_root}")
    print(f"Build cache:     {build_dir}")
    print(f"Output:          {out}{' (dry run)' if args.dry_run else ''}")
    if args.in_tree:
        print("                 (--in-tree: writing to shipped tuning_table.cuh; "
              "consider per-host instead)")

    results_by_api: dict[str, list] = {}
    for api in requested_apis:
        cfg = API_CONFIGS[api]
        shapes = shapes_for(api)
        if shapes is None:
            print(f"  → run autotune separately for {api} with matching shape "
                  f"arity {cfg['shape_keys']}.", file=sys.stderr)
            continue
        print(f"\n── {api} ({len(shapes)} shapes) " + "─" * max(0, 60 - len(api)))
        api_results = []
        for shape in shapes:
            label = cfg["label_fmt"](shape)
            print(f"  measuring {label} ...", end=" ", flush=True)
            simt, cdx = measure_shape(api, shape, sms, args.iters,
                                      mathdx_root, build_dir)
            if simt is None or cdx is None:
                print("[skipped]")
            else:
                if _cublasdx_wins(simt, cdx, args.margin):
                    winner = "cuBLASDx"
                elif simt < cdx * (1 - args.margin):
                    winner = "SIMT"
                else:
                    winner = "tie"
                print(f"simt={simt:.3f}us cublasdx={cdx:.3f}us → {winner}")
            api_results.append((shape, simt, cdx))
        results_by_api[api] = api_results

    if args.dry_run:
        print("\nDry run — not writing files.")
        return

    if args.in_tree:
        # Update the in-tree file by replacing the "measured specializations"
        # section. The round-2 primary templates and footer remain intact.
        _update_in_tree(out, sms, results_by_api, args.margin, hostname)
    else:
        # Per-host override file: raw specializations consumed via
        # GLASS_TUNING_TABLE_LOCAL.
        include_path = str(out.relative_to(GLASS_DIR)) if out.is_relative_to(GLASS_DIR) else str(out)
        emit_overrides(out, sms, results_by_api, args.margin, include_path, hostname)
    emit_results_md(md, sms, results_by_api, args.margin, hostname)

    if not args.in_tree:
        print("\nConsume the new overrides in your build with:")
        try:
            rel = out.relative_to(pathlib.Path.cwd())
            include_arg = str(rel)
        except ValueError:
            include_arg = str(out)
        print(f"  nvcc ... -DGLASS_TUNING_TABLE_LOCAL='\"{include_arg}\"' ...")


# ─── in-tree update: marker-based section replacement ──────────────────────

_IN_TREE_BEGIN = "// === BEGIN: autotune-generated specializations ==="
_IN_TREE_END   = "// === END: autotune-generated specializations ==="


def _update_in_tree(path: pathlib.Path,
                    sms: int,
                    results_by_api: dict,
                    margin: float,
                    hostname: str):
    """Replace the section bounded by the autotune markers in `path`. If the
    markers don't exist yet, insert a fresh section just before the closing
    `} // namespace _glass_tuning` (keeping round-2 primaries above it intact).
    """
    body = path.read_text()
    block_lines = [_IN_TREE_BEGIN]
    block_lines.append(f"// Generated by bench/autotune.py at "
                       f"{time.strftime('%Y-%m-%d %H:%M:%S %Z')}")
    block_lines.append(f"// Host: {hostname}    SM: sm_{sms // 10}")
    for api in API_CONFIGS:
        results = results_by_api.get(api)
        if not results:
            continue
        block_lines.append("")
        block_lines.append(f"// ─── {api} ─" + ("─" * max(0, 60 - len(api))))
        cfg = API_CONFIGS[api]
        # Sub-microsecond measurements are unreliable on small kernels
        # (timer granularity, kernel launch failures, shared-mem overflows
        # that bail before the kernel body runs all produce 0.000us-ish
        # values). Below this floor we can't trust either leg → default
        # to SIMT.
        MIN_TRUSTWORTHY_US = 0.05
        for shape, simt, cdx in results:
            spec = cfg["spec_fmt"](*shape, sms)
            if simt is None or cdx is None:
                verdict, note = "false", "measurement failed → SIMT default"
            elif simt < MIN_TRUSTWORTHY_US or cdx < MIN_TRUSTWORTHY_US:
                verdict, note = "false", (
                    f"measurement below noise floor "
                    f"(simt={simt:.3f}us, cublasdx={cdx:.3f}us; "
                    f"< {MIN_TRUSTWORTHY_US:.2f}us) → SIMT default")
            else:
                ratio = cdx / simt
                if _cublasdx_wins(simt, cdx, margin):
                    verdict, note = "true", (
                        f"cublasdx wins ({cdx:.3f}us vs simt {simt:.3f}us, "
                        f"{(1-ratio)*100:.1f}%)")
                elif ratio > (1.0 + margin):
                    verdict, note = "false", (
                        f"simt wins ({simt:.3f}us vs cublasdx {cdx:.3f}us, "
                        f"{(ratio-1)*100:.1f}%)")
                else:
                    verdict, note = "false", f"tie within ±{margin*100:.0f}%"
            block_lines.append(
                f"template <> constexpr bool {spec}() {{ return {verdict}; }} // {note}")
    block_lines.append(_IN_TREE_END)
    block = "\n".join(block_lines)

    if _IN_TREE_BEGIN in body and _IN_TREE_END in body:
        # Replace the existing block in-place.
        prefix, _, rest = body.partition(_IN_TREE_BEGIN)
        _, _, suffix = rest.partition(_IN_TREE_END)
        new_body = prefix + block + suffix
    else:
        # Insert before the closing `} // namespace _glass_tuning`.
        close = "} // namespace _glass_tuning"
        if close not in body:
            sys.exit(f"ERROR: --in-tree expected '{close}' in {path}; "
                     f"file structure changed unexpectedly. Aborting.")
        prefix, _, suffix = body.partition(close)
        new_body = prefix + block + "\n\n" + close + suffix

    path.write_text(new_body)
    n_apis = sum(1 for v in results_by_api.values() if v)
    n_total = sum(len(v) for v in results_by_api.values() if v)
