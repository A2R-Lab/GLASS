import tune_pick as tp  # the one shared tie rule (also used by bench/tune.py)

BENCH_DIR  = pathlib.Path(__file__).parent.resolve()


def _cublasdx_wins(simt, cdx, margin):
    """Shared verdict: cuBLASDx is the dependency impl, so it must clear the
    margin over SIMT. Single source of truth = tune_pick.pick."""
    return tp.pick({"simt": simt, "cublasdx": cdx}, margin, {"cublasdx"}) == "cublasdx"
GLASS_DIR  = BENCH_DIR.parent
TUNING_DIR = BENCH_DIR / "tuning"


# ─── Per-API microbench templates ───────────────────────────────────────────
#
# Each API_CONFIGS entry carries:
#   - shape_keys: tuple of shape-grid parameter names (used for parsing
#                 --shapes and for `microbench.format(**placeholders)`).
#   - default_shapes: list of tuples matching shape_keys.
#   - microbench: textwrap-dedented C++ source with `{key}` placeholders.
#                 Must compile to a binary that prints two lines:
#                     simt     <us_per_op>
#                     cublasdx <us_per_op>
#   - spec_fmt: callable (shape_values..., sms) -> the C++ specialization
#               name (e.g. "cublasdx_wins_gemv<6,6,1200>"). The output
#               file prepends "template <> constexpr bool ", appends "()
#               { return <bool>; }".
#   - label_fmt: callable (shape) -> short human label for progress prints.
#
# To add an API: append an entry to API_CONFIGS; main loop picks it up.

# NOTE: the preamble is concatenated with each per-API microbench template
# and the WHOLE combined string is passed through `str.format(**placeholders)`.
# That means literal `{` / `}` must appear as `{{` / `}}` here (and in every
# microbench template below) so .format leaves them alone.
_BENCH_PREAMBLE = textwrap.dedent("""
    // Auto-generated by bench/autotune.py
    #include <cstdio>
    #include <cstdlib>
    #include <ctime>
    #ifdef GLASS_BENCH_CUBLASDX
    #include <cublasdx.hpp>
    #endif
    #include "../glass-nvidia.cuh"

    static const int TC = 64;

    static double elapsed_us(struct timespec a, struct timespec b) {{
        return (double)(b.tv_sec - a.tv_sec) * 1e6
             + (double)(b.tv_nsec - a.tv_nsec) * 1e-3;
    }}
""").strip()


# ───── gemm ────────────────────────────────────────────────────────────────
_GEMM_MICROBENCH = _BENCH_PREAMBLE + textwrap.dedent("""

    static const int M = {M}, N = {N}, K = {K};

    namespace glass {{ namespace nvidia {{
        DEFINE_NVIDIA_GEMM_BLOCKDIM(M, N, K, TC)
    }}}}

    __global__ void k_simt(float* A, float* B, float* C, volatile float* sink, int iters) {{
        for (int rep = 0; rep < iters; rep++) {{
            glass::gemm<float, M, N, K>(1.f, A, B, 0.f, C);
            __syncthreads();
            if (threadIdx.x == 0) sink[rep & 0xFF] = C[0];
            __syncthreads();
        }}
    }}

    __global__ void k_cublasdx(float* A, float* B, float* C, volatile float* sink, int iters) {{
        extern __shared__ __align__(16) char smem[];
        for (int rep = 0; rep < iters; rep++) {{
            glass::nvidia::gemm<float, M, N, K, TC>(1.f, A, B, 0.f, C, smem);
            __syncthreads();
            if (threadIdx.x == 0) sink[rep & 0xFF] = C[0];
            __syncthreads();
        }}
    }}

    int main(int argc, char** argv) {{
        int iters = (argc > 1) ? atoi(argv[1]) : 5000;
        float *dA, *dB, *dC, *dSink;
        cudaMalloc(&dA, M*N * sizeof(float));
        cudaMalloc(&dB, N*K * sizeof(float));
        cudaMalloc(&dC, M*K * sizeof(float));
        cudaMalloc(&dSink, 256 * sizeof(float));

        struct timespec t0, t1;
        constexpr size_t smem = glass::nvidia::gemm_scratch_bytes<float, M, N, K, TC>();

        k_simt<<<1, TC>>>(dA, dB, dC, dSink, 100); cudaDeviceSynchronize();
        k_cublasdx<<<1, TC, smem>>>(dA, dB, dC, dSink, 100); cudaDeviceSynchronize();

        clock_gettime(CLOCK_MONOTONIC, &t0);
        k_simt<<<1, TC>>>(dA, dB, dC, dSink, iters); cudaDeviceSynchronize();
        clock_gettime(CLOCK_MONOTONIC, &t1);
        printf("simt %.6f\\n", elapsed_us(t0, t1) / iters);

        clock_gettime(CLOCK_MONOTONIC, &t0);
        k_cublasdx<<<1, TC, smem>>>(dA, dB, dC, dSink, iters); cudaDeviceSynchronize();
        clock_gettime(CLOCK_MONOTONIC, &t1);
        printf("cublasdx %.6f\\n", elapsed_us(t0, t1) / iters);

        cudaFree(dA); cudaFree(dB); cudaFree(dC); cudaFree(dSink);
        return 0;
    }}
""")


